public class Cabo {
  
  private float peso;
  private float tamanho;

  public Cabo(float peso, float tamanho) {
    this.peso = peso;
    this.tamanho = tamanho;
  }

  public float getPeso() {
    return peso;
  }

  public void setPeso(float peso) {
    this.peso = peso;
  }

  public float getTamanho() {
    return tamanho;
  }

  public void setTamanho(float tamanho) {
    this.tamanho = tamanho;
  }

}
