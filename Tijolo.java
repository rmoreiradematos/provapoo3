public class Tijolo {
  
  private String cor;
  private float peso;

  public Tijolo(String cor, float peso) {
    this.cor = cor;
    this.peso = peso;
  }

  public String getCor() {
    return cor;
  }

  public void setCor(String cor) {
    this.cor = cor;
  }

  public float getPeso() {
    return peso;
  }

  public void setPeso(float peso) {
    this.peso = peso;
  }
  

}
